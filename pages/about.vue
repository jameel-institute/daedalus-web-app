    <h2 id="dashboard">
      The DAEDALUS Explore dashboard
    </h2>
    <p>
      DAEDALUS Explore is an interactive dashboard tool that allows users to project the health, education and economic
      impacts of epidemic scenarios and their mitigation measures. DAEDALUS Explore allows users to simulate what would
      happen in specific countries if one of 7 hypothetical pandemics of varying severity were to
      occur. The 7 diseases
      are hypothetical but informed by the characteristics of pathogens that have caused pandemics in the past. Daedalus
      Explore supports projections for {{ numberOfCountries }} countries, and pulls in data on demography, social mixing
      patterns, and economic sector productivity and workforces from publicly available sources.
    </p>
    <p>
      Projections in DAEDALUS Explore are generated by the integrated economic-epidemiological
      <a href="https://github.com/jameel-institute/daedalus" target="_blank">Daedalus model</a>, allowing
      users to explore the model with a simplified interface and a few top-level parameter options. The Daedalus model
      is based on Doohan et al (2025)<sup>1</sup> and Haw et al (2022)<sup>2</sup>. For full details on the underlying
      Daedalus model visit the
      <a href="https://jameel-institute.github.io/daedalus/" target="_blank">Daedalus model website</a>.
      Please use the Daedalus R package for advanced use cases that are not yet supported by the dashboard.
    </p>
