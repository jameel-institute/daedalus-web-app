    <h3 id="policy-responses">
      Policy responses
    </h3>
    <p>
      The user then chooses a policy response amongst four options: No closures, school closures, business closures,
      and elimination (see <span class="table-ref">Table 2</span>). These policy responses simulate non-pharmaceutical economic
      restrictions that governments can mandate once the epidemic reaches pre-defined thresholds, as detailed below.
    </p>
    <table>
      <caption>Table 2: Response policies </caption>
      <thead>
        <tr>
          <th>Response</th>
          <th>In-person teaching in primary and secondary schools </th>
          <th>In-person work and trading for businesses </th>
        </tr>
      </thead>
      <tbody>
        <tr>
          <td>No closures</td>
          <td>yes</td>
          <td>yes</td>
        </tr>
        <tr>
          <td>School closures</td>
          <td>closure</td>
          <td>some restrictions</td>
        </tr>
        <tr>
          <td>Business closures</td>
          <td>no</td>
          <td>restrictions</td>
        </tr>
        <tr>
          <td>Elimination</td>
          <td>closure</td>
          <td>severe restrictions</td>
        </tr>
      </tbody>
    </table>
    <p>
      Under ‘Elimination’, restrictions are implemented when total hospitalisations (hospital occupancy) breach a
      country’s hospital surge capacity or 30 days after the start of the epidemic, whichever is sooner. We model
      economic restrictions as the reduction in workplace contacts due to the partial closure of economic sectors,
      which affects both transmissions and economic output. Economic restrictions are lifted again once virus
      transmission is suppressed, i.e., the effective reproduction number is below 1. Under school and business
      closures, the same thresholds for restrictions imposition and lifting apply, but only education or non-essential
      economic activity, respectively, are targeted for closures. In school closure scenarios, we assume that the lost
      present value of future earnings due to lost education is reduced due to remote education.
    </p>
    <h3 id="global-vaccine-investment">
      Global vaccine investment
    </h3>
    <p>
      Users can explore the impact of pandemic preparedness on estimated losses, specifically global investments into
      vaccines and vaccine acceptance that are funded by the international community in advance of a pandemic’s arrival.
      A higher level of investment into vaccines allows vaccination to begin earlier, be delivered at a faster rate,
      and to be accepted by a higher proportion of the population. This reduces infections and the need for costly
      closures. There are four levels of investments, ranging from none to high (see <span class="table-ref">Table 3</span>).
    </p>
