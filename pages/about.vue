    <h3 id="change-in-public-behaviour">
      Change in public behaviour
    </h3>
    <p>
      It is likely that many individuals change their behaviour and adopt precautions as cases increase and they
      perceive a high risk of infection. The user can allow for such effects and specify behaviour change ranging
      from ‘None’ which assumes no protective behaviour to ‘High’ which assumes the population perceives a high risk of
      infection, resulting in high adoption of behaviour that protects against infection. Note that behaviour change
      reduces transmissions but has not direct impact on economic output.
    </p>
    <h3 id="hospital-surge-capacity">
      Hospital surge capacity
    </h3>
    <p>
      The number of hospital beds that are available nationally for the treatment of epidemic patients is prepopulated
      with the actual value from the chosen country. Note that available beds are lower than total beds, which allows
      for the treatment of patients with other urgent conditions. Users can adjust bed capacity up or down within
      reasonable limits.
    </p>
    <h2 id="results">
      Results
    </h2>
    <h3 id="estimates-of-losses">
      Estimates of losses
    </h3>
    <p>
      The dashboard provides estimates of losses after 600 days in the chosen country. Estimated losses are:
    </p>
    <ul>
      <li>Gross Domestic Product (GDP)</li>
      <li>Education</li>
      <li>Life years</li>
    </ul>
    <p>
      Losses in GDP are the short-term losses in economic output due to business closures, which occur unless the
      ‘No closure’ response is chosen. Sickness and deaths amongst workers also result in economic losses. The
      magnitude of losses is based on economic data collected during the COVID-19 pandemic for countries that followed
      one of the three closure strategies. Lost education results in reduced life-time income for affected student.
      We use existing estimates of income reductions and the days of interrupted in-person schooling to estimate lost
      education. To measure deaths in monetary terms, we use the value-of-a-statistical-life (VSL) approach, which
      considers remaining life expectancy at death and GDP of the country to value the loss of life years
      (see <a target="_blank" href="https://jameel-institute.github.io/daedalus/articles/info_life_value.html">Value of
        life-years lost</a> for further details). Losses are provided in both % of pre-pandemic GDP and in US
      dollars. We also output life-years lost and number of deaths in natural units.
    </p>
    <h3 id="time-series">
      Time series
    </h3>
    <p>
      Time series display prevalence, hospital demand or occupancy, cumulative deaths and numbers vaccinated. Toggles
      allow the user to switch the four outcomes to numbers ‘New per day’, i.e. to infection incidence, hospitalizations,
      deaths and numbers vaccinated per day. Hospital surge capacity is indicated by the dashed red line. The blue block
      ‘Pandemic response’ shows the time period over which closures are implemented. User can download all figures and
      the underlying data in various formats and see the R code snippet for running the model with the current parameter set.
    </p>
    <p>
      DAEDALUS Explore allows users to directly compare scenarios against each other, by choosing parameters of specific
      interest. Scenarios can be compared across countries, diseases, response strategies, levels of global vaccine
      investment, change in public behaviour or hospital surge capacity. Results are displayed in stacked bars and
      tables, in total losses or as difference from the baseline.  For fine-grained control over model parameters and
      functionality, R users can run the model directly through the
      <a target="_blank" href="https://jameel-institute.github.io/daedalus/">Daedalus R package</a>.
    </p>
    <h2 id="intended-use">
      Intended use
    </h2>
    <p>
      The DAEDALUS Explore dashboard tool and the Daedalus model are projection and decision-support tools and not
      forecasting tools. The Daedalus model projects outcomes for diseases similar to pathogens that have caused
      historical pandemics. DAEDALUS Explore is not re-creating historical disease outbreaks, and it is not fitted to
      historical epidemic data. Instead, by changing the dashboard’s parameters, DAEDALUS Explore illustrates the
      projected value of differing pandemic preparedness and response activities, and in doing so, endeavours to
      support effective future pandemic planning, preparedness and response.
      The <a target="_blank" href="https://jameel-institute.github.io/daedalus/">Daedalus R package</a> offers many more
      options for customising pathogen, country, and response parameters, allowing for modelling real-time policy
      responses and their projected outcomes.
    </p>
